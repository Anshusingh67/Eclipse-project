
public class IncrementDecrementOpratorEx1 {
	public static void main(String[] args) {
		int a = 10;
		a++;
		System.out.println(a);
		
		byte b =10;
		b++;
		System.out.println(b);
		
		short c =10;
		c++;
		System.out.println(c);
	
		float d =10.2f;
		d++;
		System.out.println(d);

		
		long e =10;
		e++;
		System.out.println(e);
		

		
		double f =10;
		f++;
		System.out.println(f);    // in increment boolean and sting is not work

		
		char g ='h';
		g++;
		System.out.println(g);
	}

}
